# Integer
num = 10
print('The value of the num is: ',num, 'The data type of the number is: ',type(num))

# Float
point = 10.0
print('The value of the point is: ',point, 'The data type of the point is: ',type(point))

# Boolean
status = True
print('The value of the status is: ',status, 'The data type of the status is: ',type(status))

